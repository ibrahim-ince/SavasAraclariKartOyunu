    public void OyuncuListeGuncelle() {
        // Ekran Guncelle
        ArrayList<SavasAraclari> oyuncuKartListesi = oyuncu.getKartListesi();

        ArrayList<String> oyuncuKartListesiString = new ArrayList<>();
        String kartBilgileri;
        for (SavasAraclari kart : oyuncuKartListesi) {
            kartBilgileri = kart.KartPuaniGoster();
            oyuncuKartListesiString.add(kartBilgileri);
        }

        oyuncuKartlarList.setListData(oyuncuKartListesiString.toArray(String[]::new));
    }

    public void OyunBitti(int kazanan) {
        String oyunBittiDurum = oyun.OyunuSonlandir(kazanan);

        int response = javax.swing.JOptionPane.showConfirmDialog(null, oyunBittiDurum + "\nSkorlar:\nOyuncu: " + oyuncu.getSkor() + "\nBilgisayar: " + bilgisayar.getSkor() + "\nOyundan Cik?", "Oyun Bitti!", javax.swing.JOptionPane.YES_NO_OPTION, javax.swing.JOptionPane.WARNING_MESSAGE);

        // Uyarı penceresinde "Evet" seçildiğinde uygulamayı kapat
        if (response == javax.swing.JOptionPane.YES_OPTION) {
            System.exit(0);  // Uygulama sonlanır
        }
    }

