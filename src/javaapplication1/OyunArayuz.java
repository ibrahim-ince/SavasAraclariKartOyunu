    private void baslaButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_baslaButtonActionPerformed
        int baslangicSeviyePuani = Integer.parseInt(baslangicSeviyeTextField.getText());
        int maksimumHamleSayisi = Integer.parseInt(maksimumHamleTextField.getText());

        // Oyun Nesnesi
        oyun = new Oyun(oyuncu, bilgisayar, maksimumHamleSayisi);
        oyun.Baslat(baslangicSeviyePuani);

        OyuncuListeGuncelle();

        // Secim Bekle - Secim Tusu Aktiflestir ve Deaktifleştir
        hamleYapButton.setEnabled(true);
        baslaButton.setEnabled(false);
        baslangicSeviyeTextField.setEnabled(false);
        maksimumHamleTextField.setEnabled(false);
    }//GEN-LAST:event_baslaButtonActionPerformed

    private void hamleYapButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_hamleYapButtonActionPerformed
        // Hamle Sayisi
        hamleSayisi = Integer.parseInt(hamleSayisiLabel.getText());

        // Oyuncu Seçim Yapıyor - Secilen Kart Sayısını Kontrol Et
        int[] secimler = oyuncuKartlarList.getSelectedIndices();
        if (secimler.length == 3) {
            ArrayList<SavasAraclari> oyuncuSecilenKartlar = oyun.KartSecimi(oyuncu, secimler);

            // Kart Secimi Kontrolü
            if (!oyuncuSecilenKartlar.isEmpty()) {
                // Bilgisayar Kart Seciyor
                System.out.println("Bilgisayar Kartlarini Seciyor...");
                bilgisayar.KartlariGoster();
                System.out.println("---");
                ArrayList<SavasAraclari> bilgisayarSecilenKartlar = bilgisayar.KartSecRandom();
                System.out.println("Bilgisayar Kartlarini Secti!");

                String[][] karsilasanKartlar = oyun.HamleYap(hamleSayisi, oyuncuSecilenKartlar, bilgisayarSecilenKartlar);
                hamleSayisi++;

                // Karsilasan Kartlari Ekranda Goster
                oyuncuKartlarTextArea.setText(karsilasanKartlar[0][0] + "\n" + karsilasanKartlar[0][1] + "\n" + karsilasanKartlar[0][2]);
                bilgisayarKartlarTextArea.setText(karsilasanKartlar[1][0] + "\n" + karsilasanKartlar[1][1] + "\n" + karsilasanKartlar[1][2]);

                // Hasarları Goster
                karsilasmaTextArea.setText("1. Karsilasma: \nOzel Hasar Durumu:\nOyuncudan Bilgisayar Kartina: " + karsilasanKartlar[8][0] + "\nBilgisayardan Oyuncu Kartina: " + karsilasanKartlar[8][1] + "\nOyuncudan Bilgisayar Kartina Hasar: " + karsilasanKartlar[7][0] + "\n" + "Bilgisayardan Oyuncu Kartina Hasar: " + karsilasanKartlar[7][1] + "\n\n2. Karsilasma: \nOzel Hasar Durumu:\nOyuncudan Bilgisayar Kartina: " + karsilasanKartlar[8][2] + "\nBilgisayardan Oyuncu Kartina: " + karsilasanKartlar[8][3] + "\nOyuncudan Bilgisayar Kartina Hasar: " + karsilasanKartlar[7][2] + "\n" + "Bilgisayardan Oyuncu Kartina Hasar: " + karsilasanKartlar[7][3] + "\n\n3. Karsilasma: \nOzel Hasar Durumu:\nOyuncudan Bilgisayar Kartina: " + karsilasanKartlar[8][4] + "\nBilgisayardan Oyuncu Kartina: " + karsilasanKartlar[8][5] + "\nOyuncudan Bilgisayar Kartina Hasar: " + karsilasanKartlar[7][4] + "\n" + "Bilgisayardan Oyuncu Kartina Hasar: " + karsilasanKartlar[7][5]);

                // Karşılaşma Sonrası Kartları Ekranda Göster
                oyuncuHamleTextArea.setText(karsilasanKartlar[2][0] + karsilasanKartlar[5][0] + "\n" + karsilasanKartlar[2][1] + karsilasanKartlar[5][1] + "\n" + karsilasanKartlar[2][2] + karsilasanKartlar[5][2]);
                bilgisayarHamleTextArea.setText(karsilasanKartlar[3][0] + karsilasanKartlar[6][0] + "\n" + karsilasanKartlar[3][1] + karsilasanKartlar[6][1] + "\n" + karsilasanKartlar[3][2] + karsilasanKartlar[6][2]);

                // Skorları Goster
                oyuncuSkorLabel.setText(karsilasanKartlar[4][0]);
                bilgisayarSkorLabel.setText(karsilasanKartlar[4][1]);

                // Hamle Sayısını Ekranda Guncelle
                hamleSayisiLabel.setText(String.valueOf(hamleSayisi));

                // Hamle Bitir Buttonu Aktif Et ve Hamle Yap Button Deaktif Et
                hamleBitirButton.setEnabled(true);
                hamleYapButton.setEnabled(false);
            } else {
                javax.swing.JOptionPane.showMessageDialog(null, "Tum Kartlari Birer Kez Secmeden Ayni Karti Tekrar Kullanamazsiniz!", "Uyarı", javax.swing.JOptionPane.WARNING_MESSAGE);
            }
        } else {
            javax.swing.JOptionPane.showMessageDialog(null, "3 Kart Secmelisiniz!", "Uyarı", javax.swing.JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_hamleYapButtonActionPerformed

    private void hamleBitirButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_hamleBitirButtonActionPerformed
        // Ekranı Temizle, Kart Dağıt ve Oyuncu Kart Ekranını Güncelle
        oyuncuKartlarTextArea.setText("");
        bilgisayarKartlarTextArea.setText("");
        karsilasmaTextArea.setText("");
        oyuncuHamleTextArea.setText("");
        bilgisayarHamleTextArea.setText("");

        System.out.println("----Kalan Kartlar----");
        System.out.println("Oyuncu: " + oyuncu.getKartListesi().size() + " - Bilgisayar: " + bilgisayar.getKartListesi().size());

        // Oyun Bitti mi?
        // Karti biten var mi?
        if (oyuncu.getKartListesi().isEmpty()) {
            kazanan = 2;
            OyuncuListeGuncelle();
            OyunBitti(kazanan);
        } else if (bilgisayar.getKartListesi().isEmpty()) {
            kazanan = 1;
            OyuncuListeGuncelle();
            OyunBitti(kazanan);
        } else if (Integer.parseInt(hamleSayisiLabel.getText()) == Integer.parseInt(maksimumHamleTextField.getText())) { // Maksimum Hamle Bitti mi?
            OyuncuListeGuncelle();
            OyunBitti(kazanan);
        } else {
            // Yeni Kartlari Ver
            oyun.YeniKartVer(Integer.parseInt(baslangicSeviyeTextField.getText()));
            System.out.println("Birer Kart Verildi: " + oyuncu.getKartListesi().size() + " - " + bilgisayar.getKartListesi().size());

            // Kart Secilme Durumu Guncelle
            oyuncu.KartKontrolGuncelle();

            OyuncuListeGuncelle();

            // Secim Bekle - Secim Tusu Aktiflestir ve Deaktiflestir
            hamleYapButton.setEnabled(true);
            hamleBitirButton.setEnabled(false);
        }

    }//GEN-LAST:event_hamleBitirButtonActionPerformed

